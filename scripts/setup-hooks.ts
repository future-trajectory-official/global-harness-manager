import { join } from "jsr:@std/path";

/**
 * .git/hooks/pre-push を設定するスクリプト。
 * 開発者が Push 前に自動で deno task test 等が走るようにします。
 */

async function main() {
  const currentDir = Deno.cwd();
  const gitHooksDir = join(currentDir, ".git", "hooks");
  const prePushPath = join(gitHooksDir, "pre-push");

  // .git ディレクトリが存在するか確認
  try {
    const stat = await Deno.stat(join(currentDir, ".git"));
    if (!stat.isDirectory) {
      throw new Error(".git is not a directory");
    }
  } catch (_e) {
    console.error("エラー: 現在のディレクトリは Git リポジトリではありません。");
    Deno.exit(1);
  }

  // pre-push フックのスクリプト内容
  const hookScript = `#!/bin/sh
# pre-push hook generated by deno task setup-hooks

echo "Running checks before push..."

echo "[1/3] Format check..."
deno fmt --check
if [ $? -ne 0 ]; then
  echo "❌ Format check failed. Run 'deno fmt' to fix."
  exit 1
fi

echo "[2/3] Lint check..."
deno lint
if [ $? -ne 0 ]; then
  echo "❌ Lint check failed."
  exit 1
fi

echo "[3/3] Tests..."
deno task test
if [ $? -ne 0 ]; then
  echo "❌ Tests failed."
  exit 1
fi

echo "✅ All checks passed!"
exit 0
`;

  try {
    await Deno.writeTextFile(prePushPath, hookScript);

    // Windows 以外の環境では実行権限を付与する
    if (Deno.build.os !== "windows") {
      const chmodCmd = new Deno.Command("chmod", {
        args: ["+x", prePushPath],
      });
      await chmodCmd.output();
    }

    console.log("✅ pre-push hook を正常に設定しました！");
    console.log(`設定先: ${prePushPath}`);
  } catch (error) {
    console.error("エラー: pre-push hook の設定に失敗しました。", error);
    Deno.exit(1);
  }
}

if (import.meta.main) {
  main();
}
